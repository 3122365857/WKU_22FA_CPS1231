package chapter2;

//Purpose: Compute the area of a circle
//          with radius 10.
public class CircleArea {

	public static void main(String[] args) {
		//1. Define/represent PI
		double pi = 3.14159;
		
		//2. Define radius: 10
		int radius = 10;
		
		//3. Compute the area:
		//    area = PI*r*r
		double area = pi*radius*radius;
		
		//4. Show/print the area
		System.out.println("The area of a circle"
				+ " with radius 10 is "+ area);
		
		
		
		//Primitive Data: int, double
		//    int --> integer data type:
		//       e.g. 1, 2, 3 ,77767
		//            66
		//    double -->double precision floating point number
		//        e.g.   3.3444,  8.42432234, 1.5
		
		
	}

}
